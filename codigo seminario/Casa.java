public class Casa{
    private int janela;
    private int portas;
    private int comodos;
    private boolean temGaragem;
    private boolean temPiscina;
    private boolean temEstatuas;
    private boolean temJardim;

    public Casa(int janela, int portas, int comodos, boolean temGaragem, boolean temPiscina, boolean temEstatuas, boolean temJardim){
        this.janela = janela;
        this.portas = portas;
        this.comodos = comodos;
        this.temGaragem = temGaragem;
        this.temPiscina = temPiscina;
        this.temEstatuas = temEstatuas;
        this.temJardim = temJardim;
    }


}